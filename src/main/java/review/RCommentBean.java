package review;

public class RCommentBean {
	private int num;
	private String uid;
	private String upass;
	private String content;
	private String wdate;
	private int ref;
	private String commentAvailable;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getUpass() {
		return upass;
	}
	public void setUpass(String upass) {
		this.upass = upass;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWdate() {
		return wdate;
	}
	public void setWdate(String wdate) {
		this.wdate = wdate;
	}
	public int getRef() {
		return ref;
	}
	public void setRef(int ref) {
		this.ref = ref;
	}
	public String getCommentAvailable() {
		return commentAvailable;
	}
	public void setCommentAvailable(String commentAvailable) {
		this.commentAvailable = commentAvailable;
	}
		
	
}
